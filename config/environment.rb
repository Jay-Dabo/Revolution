class Date
      def to_time(form = :local)
        ::Time.send("#{form}", year, month, day)
      end
      
      def qtr
        month = self.strftime('%m').to_i
        case month
        when (1..3)
          '1'
        when (4..6)
          '2'
        when (7..9)
          '3'
        when (10..12)
          '4'
        end
      end

      def year_qtr
        year = self.strftime('%Y')
        year + '-' + self.qtr
      end
end

class Time
    def end_of_day
        change(:hour => 23, :min => 59, :sec => 59)
    end
    
    def end_of_week
      days_to_sunday = self.wday!=0 ? 7-self.wday : 0
      (self + days_to_sunday.days).end_of_day
    end
    
end

class Array
   def to_hash_keys(&block)
     Hash[*self.collect { |v|
       [v, block.call(v)]
     }.flatten]
   end
 
   def to_hash_values(&block)
     Hash[*self.collect { |v|
       [block.call(v), v]
     }.flatten]
   end
end

